class UsersController < ApplicationController
  before_action :set_user, only: [:show, :destroy, :update, :edit]
  load_and_authorize_resource

  has_scope :by_role
  
  

  def index
    @q = apply_scopes(User).all.search(params[:q])
    @users = @q.result(distinct: true)
  end

  # GET /users/1
